-- Validacao segura: nao altera musicas. Se houver musicas com ministerio_id invalido,
-- a FK fica NOT VALID, mas novos dados passam a ser protegidos.
do $$
begin
    if not exists (
        select 1
          from musicas mu
         where mu.ministerio_id is not null
           and not exists (
                select 1
                  from ministerios m
                 where m.id = mu.ministerio_id
           )
    ) then
        alter table musicas validate constraint fk_musicas_ministerio;
    else
        raise notice 'fk_musicas_ministerio ficou NOT VALID porque existem musicas com ministerio_id sem ministerio correspondente.';
    end if;
end $$;

