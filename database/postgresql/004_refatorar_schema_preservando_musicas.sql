end $$;

create or replace function pg_temp.wf_ensure_id_sequence(p_table regclass, p_column name)
returns void
language plpgsql
as $$
declare
    v_schema_name name;
    v_table_name name;
    v_table_ref text;
    v_sequence_name name;
    v_sequence_ref text;
    v_is_identity text;
    v_column_default text;
    v_max_id bigint;
begin
    select n.nspname, c.relname
      into v_schema_name, v_table_name
      from pg_class c
      join pg_namespace n on n.oid = c.relnamespace
     where c.oid = p_table;

    v_table_ref := format('%I.%I', v_schema_name, v_table_name);
    v_sequence_name := v_table_name || '_' || p_column || '_seq';
    v_sequence_ref := format('%I.%I', v_schema_name, v_sequence_name);

    select attidentity
      into v_is_identity
      from pg_attribute
     where attrelid = p_table
       and attname = p_column
       and not attisdropped;

    if v_is_identity in ('a', 'd') then
        return;
    end if;

    select column_default
      into v_column_default
      from information_schema.columns
     where table_schema = v_schema_name
       and table_name = v_table_name
       and column_name = p_column;

    if v_column_default is not null and v_column_default like 'nextval(%' then
        execute format('select coalesce(max(%I), 0) from %s', p_column, v_table_ref)
           into v_max_id;
        execute format('select setval(pg_get_serial_sequence(%L, %L), %s, %s)',
                       v_table_ref,
                       p_column,
                       greatest(v_max_id, 1),
                       case when v_max_id > 0 then 'true' else 'false' end);
        return;
    end if;

    execute format('create sequence if not exists %s', v_sequence_ref);
    execute format('alter sequence %s owned by %s.%I', v_sequence_ref, v_table_ref, p_column);
    execute format('alter table %s alter column %I set default nextval(%L::regclass)',
                   v_table_ref,
                   p_column,
                   v_sequence_ref);
    execute format('select coalesce(max(%I), 0) from %s', p_column, v_table_ref)
       into v_max_id;
    execute format('select setval(%L::regclass, %s, %s)',
                   v_sequence_ref,
                   greatest(v_max_id, 1),
                   case when v_max_id > 0 then 'true' else 'false' end);
end $$;

select pg_temp.wf_ensure_id_sequence('ministerios'::regclass, 'id');
select pg_temp.wf_ensure_id_sequence('musicas'::regclass, 'id');
select pg_temp.wf_ensure_id_sequence('usuarios'::regclass, 'id');
select pg_temp.wf_ensure_id_sequence('escalas'::regclass, 'id');
select pg_temp.wf_ensure_id_sequence('repertorios'::regclass, 'id');

do $$
declare
    r record;
begin
    for r in
        select conrelid::regclass as table_name, conname
          from pg_constraint
         where contype = 'f'
           and conrelid in (
                'musicas'::regclass,
                'usuarios'::regclass,
                'escalas'::regclass,
                'repertorios'::regclass,
                'escala_musicas'::regclass,
                'escala_usuarios'::regclass,
                'repertorio_musicas'::regclass,
                'usuario_musicas_favoritas'::regclass
           )
    loop
        execute format('alter table %s drop constraint if exists %I', r.table_name, r.conname);
    end loop;
end $$;

do $$
begin
    if not exists (select 1 from pg_constraint where conrelid = 'ministerios'::regclass and contype = 'p') then
        alter table ministerios add constraint ministerios_pkey primary key (id);
    end if;

    if not exists (select 1 from pg_constraint where conrelid = 'musicas'::regclass and contype = 'p') then
        alter table musicas add constraint musicas_pkey primary key (id);
    end if;

    if not exists (select 1 from pg_constraint where conrelid = 'usuarios'::regclass and contype = 'p') then
        alter table usuarios add constraint usuarios_pkey primary key (id);
    end if;

    if not exists (select 1 from pg_constraint where conrelid = 'escalas'::regclass and contype = 'p') then
        alter table escalas add constraint escalas_pkey primary key (id);
    end if;

    if not exists (select 1 from pg_constraint where conrelid = 'repertorios'::regclass and contype = 'p') then
        alter table repertorios add constraint repertorios_pkey primary key (id);
    end if;

    if not exists (select 1 from pg_constraint where conname = 'usuarios_email_key') then
