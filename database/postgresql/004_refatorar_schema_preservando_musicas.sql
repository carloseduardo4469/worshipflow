    if not exists (select 1 from pg_constraint where conname = 'repertorio_musicas_pkey') then
        alter table repertorio_musicas add constraint repertorio_musicas_pkey primary key (repertorio_id, musica_id);
    end if;

    if not exists (select 1 from pg_constraint where conname = 'usuario_musicas_favoritas_pkey') then
        alter table usuario_musicas_favoritas add constraint usuario_musicas_favoritas_pkey primary key (usuario_id, musica_id);
    end if;
end $$;

