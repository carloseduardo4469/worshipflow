alter table usuarios alter column status_ministerio set default 'ATIVO';
alter table usuarios alter column perfil set default 'USER';
alter table escalas alter column status set default 'RASCUNHO';

do $$
begin
    if not exists (select 1 from musicas where titulo is null) then
        alter table musicas alter column titulo set not null;
    else
        raise notice 'Nao foi aplicado NOT NULL em musicas.titulo porque existem musicas com titulo nulo.';
