-- WorshipFlow - refatoracao do schema PostgreSQL preservando registros de musicas.
--
-- Objetivo:
-- 1. Alinhar o banco antigo ao modelo atual das entidades Spring Boot.
-- 2. Preservar todos os registros existentes em public.musicas.
-- 3. Remover registros operacionais das demais tabelas quando isso nao quebra
--    a integridade das musicas preservadas.
--
-- Observacao importante:
-- - Registros de ministerios referenciados por musicas precisam ser mantidos.
--   Apagar esses ministerios obrigaria alterar musicas.ministerio_id, o que
--   violaria a regra de nao mexer nos registros da tabela musicas.

